def _next_fallback() -> dict:
    global _fallback_index
    article = dict(NEWS_POOL[_fallback_index])
    article["timestamp"] = int(time.time())
    _fallback_index = (_fallback_index + 1) % len(NEWS_POOL)
    return article


class ConnectionManager:
    def __init__(self):
        self.active_connections: list[WebSocket] = []

    async def connect(self, websocket: WebSocket):
        await websocket.accept()
        self.active_connections.append(websocket)
        logger.info(f"News WebSocket client connected. Total: {len(self.active_connections)}")

    def disconnect(self, websocket: WebSocket):
        self.active_connections.remove(websocket)
        logger.info(f"News WebSocket client disconnected. Total: {len(self.active_connections)}")

    async def broadcast(self, message: str):
        dead: list[WebSocket] = []
        for connection in self.active_connections:
            try:
                await connection.send_text(message)
            except Exception:
                dead.append(connection)
        for conn in dead:
            self.active_connections.remove(conn)


manager = ConnectionManager()


async def _fetch_feed(url: str) -> list[dict]:
    try:
        async with httpx.AsyncClient(timeout=15, headers={"User-Agent": "Mozilla/5.0"}) as client:
            resp = await client.get(url, follow_redirects=True)
            resp.raise_for_status()
    except Exception as e:
        logger.warning(f"HTTP error fetching {url}: {e}")
        return []
    feed = feedparser.parse(resp.text)
    articles: list[dict] = []
    for entry in feed.entries:
        article = _entry_to_article(entry)
        if article is not None:
            async with _seen_lock:
                aid = _article_id(article["title"], article["link"])
                if aid not in _seen_ids:
                    _seen_ids.add(aid)
                    articles.append(article)
    logger.info(f"Fetched {len(articles)} new articles from {url}")
    return articles


async def _poll_once():
    all_new: list[dict] = []
    for feed_url in RSS_FEEDS:
        new_articles = await _fetch_feed(feed_url)
        all_new.extend(new_articles)
    if not all_new:
        logger.info("No new articles from RSS feeds, using fallback pool")
        article = _next_fallback()
        if manager.active_connections:
            await manager.broadcast(json.dumps(article))
        return
    random.shuffle(all_new)
    for article in all_new:
        if manager.active_connections:
            await manager.broadcast(json.dumps(article))
            await asyncio.sleep(0.5)


async def _poll_loop():
    logger.info("News poller loop started")
    while not _stop_event.is_set():
        try:
            await _poll_once()
        except Exception as e:
            logger.error(f"Poll error: {e}", exc_info=True)
            if manager.active_connections:
                article = _next_fallback()
                await manager.broadcast(json.dumps(article))
        try:
            await asyncio.wait_for(_stop_event.wait(), timeout=POLL_INTERVAL)
        except asyncio.TimeoutError:
            continue


def start_poller(app=None):
    global _poller_task
    if _poller_task is None or _poller_task.done():
        _stop_event.clear()
        _poller_task = asyncio.create_task(_poll_loop())
        logger.info("News poller started")


def stop_poller():
    _stop_event.set()
    if _poller_task and not _poller_task.done():
        _poller_task.cancel()
    logger.info("News poller stopped")


