def _article_id(title: str, url: str) -> str:
    raw = (title or "") + (url or "")
    return hashlib.sha256(raw.encode()).hexdigest()[:16]


def _categorize(headline: str, summary: str) -> str:
    text = (headline + " " + summary).lower()
    for keywords, category in CATEGORY_RULES:
        if any(kw in text for kw in keywords):
            return category
    return "general"


def _clean_html(text: str) -> str:
    return re.sub(r"<[^>]+>", "", text)


def _entry_to_article(entry) -> dict | None:
    title = entry.get("title", "")
    link = entry.get("link", "")
    id_ = _article_id(title, link)
    summary = _clean_html(entry.get("summary", "") or entry.get("description", "") or "")
    if not title and not summary:
        return None
    source = getattr(entry, "source", None)
    src = getattr(source, "title", None) if source else None
    if not src:
        src = random.choice(SOURCES)
    ts = int(time.time())
    cat = _categorize(title, summary)
    return {
        "id": f"news-{id_}",
        "headline": title,
        "title": title,
        "summary": summary[:300],
        "description": summary[:1000],
        "source": src,
        "link": link,
        "url": link,
        "category": cat,
        "timestamp": ts,
        "tier": 2 if len(summary) < 100 else 1,
    }


